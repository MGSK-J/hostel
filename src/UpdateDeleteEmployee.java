    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         String mobileNo=jTextField1.getText();
        try
        {
          Connection con=ConnectionProvider.getCon();
          Statement st=con.createStatement();
          ResultSet rs=st.executeQuery("select * from employee where mobileNo='"+mobileNo+"'");
          if(rs.first())
          {
            jTextField1.setEditable(false);
            jTextField2.setText(rs.getString(2));
            jTextField3.setText(rs.getString(3));
            jTextField4.setText(rs.getString(4));
            jTextField5.setText(rs.getString(5));
            jTextField6.setText(rs.getString(6));
            jTextField7.setText(rs.getString(7));
            jTextField8.setText(rs.getString(8));
            if(rs.getString(9).equals("Working"))
            {
                jComboBox1.addItem("Working");
                jComboBox1.addItem("Not Working");
            }
            else
            {
                jComboBox1.addItem("Not Working");
                jComboBox1.addItem("Working");
            }
          }
          else
          {
            JOptionPane.showMessageDialog(null, "Employee does not exist");
            clear();
          }
          }
          catch(Exception e)
            {
              JOptionPane.showMessageDialog(null, e);
            }
      
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
         String mobileNo=jTextField1.getText();
        String name=jTextField2.getText();
        String father=jTextField3.getText();
        String mother=jTextField4.getText();
        String email=jTextField5.getText();
        String address=jTextField6.getText();
        String nic=jTextField7.getText();
        String status=(String)jComboBox1.getSelectedItem();
        try
        {
           Connection con=ConnectionProvider.getCon();
           PreparedStatement ps=con.prepareStatement("update employee set name=?,father=?,mother=?,email=?,address=?,nic=?,status=? where mobileNo=?");
           ps.setString(1,name);
           ps.setString(2,father);
           ps.setString(3,mother);
           ps.setString(4,email);
           ps.setString(5,address);
           ps.setString(6,nic);
           ps.setString(7,status);
           ps.setString(8,mobileNo);
           ps.executeUpdate();
           JOptionPane.showMessageDialog(null,"Successfully Updated");
           clear();
        }
        catch(Exception e)
        {
           JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

